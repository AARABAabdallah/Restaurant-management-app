    public void Afficher(){
        try{
            String jour = "lundi";
            String repas = "petit_dejeuner";
            txt_jour.setText(jour);
            txt_repas.setText(repas);
            //ArrayList<Integer> listeIdPlats = new ArrayList<Integer>();
            String sql = " select id_plat from composition where id_menu = ? ";
            pst = conn.prepareStatement(sql);
            pst.setInt(1, 1);
            rs = pst.executeQuery();
            int count = 0;
            while(rs.next()){
                //Integer I = new Integer(rs.getInt(1));
                //listeIdPlats.add(I);
                count++;
            }
            if(count != 0){
                String sql1 = "select nom_plat , designation_article , quantite_article_plat FROM " +
                "((SELECT cmp.id_plat, nom_plat FROM plat p JOIN " +
                "composition cmp ON p.id_plat = cmp.id_plat WHERE cmp.id_menu = ? ) R " +
                "INNER JOIN " +
                "(SELECT designation_article , id_plat , quantite_article_plat FROM article art JOIN liste_articles_du_plat la ON " +
                "art.id_article = la.id_article) k " +
                "ON r.id_plat = k.id_plat) ";
                pst = conn.prepareStatement(sql1);
                pst.setInt(1,idMenu);
                rs = pst.executeQuery();
                table_join.setModel(DbUtils.resultSetToTableModel(rs));
            }
            else{
                JOptionPane.showMessageDialog(null, " Pas encore de plats inscrits dans ce menu l√† ");
            }
        }catch(Exception e ){
            e.printStackTrace();
        }finally{
            
        }
    }
    
